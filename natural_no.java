import java.util.Scanner;
public class natural_no {
    public static void main(String args[]) {
        Scanner sc=new Scanner(System.in);
        int n;
        System.out.println("enter a value of n");
        n=sc.nextInt();
        int i;
        i=i;
        while(i<=n){
            System.out.println(i);
            i++;
        }
        
        sc.close();
    }
    
}
